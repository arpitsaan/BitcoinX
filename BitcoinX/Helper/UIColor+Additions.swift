import UIKit

extension UIColor {
    struct bxDarkTheme {
        static var orange: UIColor  {
            return UIColor(red:250/255.0, green:160/255.0, blue:35/255.0, alpha:1)
        }
        
        static var gray: UIColor {
            return UIColor(red:137/255.0, green:149/255.0, blue:155/255.0, alpha:1)
        }
        
        static var darkBg: UIColor {
            return UIColor(red:34/255.0, green:34/255.0, blue:35/255.0, alpha:1)
        }
    }
}
